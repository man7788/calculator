function add(num1, num2) {
 result = num1 + num2;
 return result;
}

function subtract(baseNum, subtractNum) {
  result = baseNum - subtractNum;
  return result;
 }

 function multiply(num1, num2) {
  result = num1 * num2;
  return result;
 }

 function divide(dividend, divisor) {
  result = dividend / divisor;
  return result;
 }